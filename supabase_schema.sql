returns trigger as $$
declare
  item_count integer;
begin
  select count(*) into item_count from wishlist where user_id = auth.uid();
  
  -- If we already have 5, and we're trying to add another one, stop it.
  if item_count >= 5 then
    raise exception 'Wishlist limit reached. You can only add up to 5 items.';
  end if;
  
  return new;
end;
$$ language plpgsql;

