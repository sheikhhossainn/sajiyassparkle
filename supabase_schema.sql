create table contact_messages (
  id bigint generated by default as identity primary key,
  thread_id bigint not null references contact_threads(id) on delete cascade,
  sender_id uuid references profiles(id) on delete set null,
  sender_role text not null check (sender_role in ('customer', 'admin')),
  message text not null,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

create index idx_contact_threads_customer_id on contact_threads(customer_id);
create index idx_contact_threads_status on contact_threads(status);
create index idx_contact_threads_last_message_at on contact_threads(last_message_at desc);
create index idx_contact_messages_thread_id_created_at on contact_messages(thread_id, created_at);

alter table contact_threads enable row level security;
alter table contact_messages enable row level security;

create policy "Admins can read all threads"
  on contact_threads for select
  using ( exists ( select 1 from profiles where id = auth.uid() and is_admin = true ) );

create policy "Customers can read own threads"
  on contact_threads for select
  using ( customer_id = auth.uid() );

create policy "Anyone can create contact thread"
  on contact_threads for insert
  with check ( true );

create policy "Admins can update threads"
  on contact_threads for update
  using ( exists ( select 1 from profiles where id = auth.uid() and is_admin = true ) );

create policy "Admins can read all messages"
  on contact_messages for select
  using (
    exists ( select 1 from profiles where id = auth.uid() and is_admin = true )
    or exists (
      select 1
      from contact_threads ct
      where ct.id = contact_messages.thread_id
        and ct.customer_id = auth.uid()
    )
  );

create policy "Anyone can create customer message"
  on contact_messages for insert
  with check ( sender_role = 'customer' );

create policy "Admins can create admin message"
  on contact_messages for insert
  with check (
    sender_role = 'admin'
    and exists ( select 1 from profiles where id = auth.uid() and is_admin = true )
  );

create or replace function public.bump_contact_thread_last_message()
returns trigger
language plpgsql
