create or replace function check_wishlist_limit()
returns trigger as $tag$
declare
  item_count integer;
begin
  select count(*) into item_count from wishlist where user_id = auth.uid();
  
  if item_count >= 5 then
    raise exception 'Wishlist limit reached. You can only add up to 5 items.';
  end if;
  
  return new;
end;
$tag$ language plpgsql;

create trigger enforce_wishlist_limit
  before insert on wishlist
  for each row
  execute procedure check_wishlist_limit();

