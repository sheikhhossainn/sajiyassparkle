-- Function to add a new admin (with limit of 2 additional admins)
create or replace function add_admin(target_email text)
returns json
language plpgsql
security definer
as $tag$
declare
  target_id uuid;
  admin_count integer;
  current_user_super boolean;
begin
  -- Check if caller is Super Admin
  select is_super_admin into current_user_super from profiles where id = auth.uid();
  if not current_user_super then
    return json_build_object('success', false, 'message', 'Only Super Admins can add admins.');
  end if;

  -- Check limit (Total non-super admins)
  -- 'Real Admin' is Super Admin. '2 more admins' means regular admins.
  select count(*) into admin_count from profiles where is_admin = true and is_super_admin = false;
  
  if admin_count >= 2 then
    return json_build_object('success', false, 'message', 'Maximum limit of 2 additional admins reached.');
  end if;

  -- Find target user
  select id into target_id from profiles where email = target_email;
  
  if target_id is null then
    return json_build_object('success', false, 'message', 'User with this email not found.');
  end if;

  -- Update target to Admin
  update profiles set is_admin = true where id = target_id;
  
  return json_build_object('success', true, 'message', 'Admin added successfully.');
end;
$tag$;

-- Function to remove an admin
create or replace function remove_admin(target_id uuid)
returns json
language plpgsql
security definer
as $tag$
declare
  current_user_super boolean;
  target_super boolean;
begin
  -- Check if caller is Super Admin
  select is_super_admin into current_user_super from profiles where id = auth.uid();
  if not current_user_super then
    return json_build_object('success', false, 'message', 'Only Super Admins can remove admins.');
  end if;

  -- Check targeting self or another Super Admin
  select is_super_admin into target_super from profiles where id = target_id;
  
  if target_super then
    return json_build_object('success', false, 'message', 'Cannot remove Super Admin access.');
  end if;

  -- Demote target
  update profiles set is_admin = false where id = target_id;
  
  return json_build_object('success', true, 'message', 'Admin removed successfully.');
end;
$tag$;

-- Grant permissions
GRANT EXECUTE ON FUNCTION add_admin(text) TO authenticated;
GRANT EXECUTE ON FUNCTION remove_admin(uuid) TO authenticated;


-- ==========================================
-- 10. MIGRATIONS (Fix existing admins)
-- ==========================================
-- Ensure at least one super admin exists (Promote the first admin found if none exist)
-- This is critical if the schema is updated but data is preserved (e.g. via alter table, or just running in SQL editor)
do \$\$
declare
  admin_exists boolean;
  super_admin_exists boolean;
begin
  -- Check if any admin exists
  select count(*) > 0 into admin_exists from profiles where is_admin = true;
  
  -- Check if any super admin exists
  select count(*) > 0 into super_admin_exists from profiles where is_super_admin = true;
  
  -- If admins exist but NO super admin exists, promote the oldest admin
  if admin_exists and not super_admin_exists then
    update profiles 
    set is_super_admin = true 
    where id = (
      select id 
      from profiles 
      where is_admin = true 
      order by created_at asc 
      limit 1
    );
  end if;
end \$\$;


