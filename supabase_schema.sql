-- ==========================================
-- 0. CLEANUP (Run this to reset tables)
-- WARNING: This will delete existing products and profiles!
-- ==========================================
drop table if exists order_items cascade;
drop table if exists orders cascade;
drop table if exists products cascade;
drop table if exists profiles cascade;
drop policy if exists "Public Access" on storage.objects;
drop policy if exists "Admin Upload" on storage.objects;
drop policy if exists "Admin Delete" on storage.objects;

-- ==========================================
-- 1. PROFILES (Users & Admins)
-- ==========================================
create table profiles (
  id uuid references auth.users not null primary key,
  email text,
  username text,
  is_admin boolean default false,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable RLS
alter table profiles enable row level security;

-- Policies
create policy "Public profiles are viewable by everyone" 
  on profiles for select using ( true );

create policy "Users can insert their own profile" 
  on profiles for insert with check ( auth.uid() = id );

create policy "Users can update own profile" 
  on profiles for update using ( auth.uid() = id );

-- ==========================================
-- 2. PRODUCTS (Catalog)
-- ==========================================
create table products (
  id bigint generated by default as identity primary key,
  name text not null,
  category text default 'General',
  price numeric not null,
  image_url text not null,
  featured boolean default false,
  description text,
  stock_status text default 'In Stock', -- 'In Stock', 'Out of Stock'
  stock_quantity integer default 0,
  created_at timestamp with time zone default timezone('utc'::text, now()) not null,
  updated_at timestamp with time zone default timezone('utc'::text, now())
);

-- Enable RLS
alter table products enable row level security;

-- Policies
create policy "Products are viewable by everyone" 
  on products for select using ( true );

create policy "Admins can insert products" 
  on products for insert 
  with check ( exists ( select 1 from profiles where id = auth.uid() and is_admin = true ) );

create policy "Admins can update products" 
  on products for update 
  using ( exists ( select 1 from profiles where id = auth.uid() and is_admin = true ) );

create policy "Admins can delete products" 
  on products for delete 
  using ( exists ( select 1 from profiles where id = auth.uid() and is_admin = true ) );

-- ==========================================
-- 3. ORDERS (Sales Tracking)
-- ==========================================
create table orders (
  id bigint generated by default as identity primary key,
  user_id uuid references profiles(id),
  total_amount numeric not null,
  status text default 'pending', -- pending, completed, cancelled
  created_at timestamp with time zone default timezone('utc'::text, now()) not null
);

-- Enable RLS
alter table orders enable row level security;

-- Policies
create policy "Users can view their own orders" 
  on orders for select using ( auth.uid() = user_id );

create policy "Admins can view all orders" 
  on orders for select 
  using ( exists ( select 1 from profiles where id = auth.uid() and is_admin = true ) );

create policy "Users can create orders" 
  on orders for insert with check ( auth.uid() = user_id );

-- ==========================================
-- 4. ORDER ITEMS (Best Seller Analytics)
-- ==========================================
create table order_items (
  id bigint generated by default as identity primary key,
  order_id bigint references orders(id) on delete cascade,
  product_id bigint references products(id),
  quantity integer default 1,
  price_at_purchase numeric not null -- In case product price changes later
);

-- Enable RLS
alter table order_items enable row level security;

-- Policies
create policy "Users can view their own order items" 
  on order_items for select 
  using ( exists ( select 1 from orders where orders.id = order_items.order_id and orders.user_id = auth.uid() ) );

create policy "Admins can view all order items" 
  on order_items for select 
  using ( exists ( select 1 from profiles where id = auth.uid() and is_admin = true ) );

create policy "Users can insert order items" 
  on order_items for insert with check ( true ); 

-- ==========================================
-- 5. STORAGE BUCKETS & POLICIES
-- ==========================================

-- Create the 'products' bucket with constraints
-- Note: limit is in bytes. 5MB = 5 * 1024 * 1024 = 5242880
insert into storage.buckets (id, name, public, file_size_limit, allowed_mime_types)
values (
  'products', 
  'products', 
  true, 
  5242880, 
  ARRAY['image/png', 'image/jpeg', 'image/webp', 'image/jpg']
) on conflict (id) do nothing;

-- Policy: Public Read Access (Everyone can see images)
create policy "Public Access"
  on storage.objects for select
  using ( bucket_id = 'products' );

-- Policy: Admin Upload Access (Only admins can upload)
create policy "Admin Upload"
  on storage.objects for insert
  with check ( 
    bucket_id = 'products' 
    and exists ( select 1 from public.profiles where id = auth.uid() and is_admin = true )
  );

-- ==========================================
-- 6. TRIGGERS (Auto-Profile Creation)
-- ==========================================

-- Function to handle new user signup
create or replace function public.handle_new_user()
returns trigger as $$
declare
  raw_full_name text;
  raw_first_name text;
  raw_last_name text;
begin
  -- Get user metadata safely
  raw_full_name := new.raw_user_meta_data->>'full_name';
  raw_first_name := new.raw_user_meta_data->>'first_name';
  raw_last_name := new.raw_user_meta_data->>'last_name';
  
  -- If first/last name not available, try to parse full_name or email
  if raw_first_name is null then
    if raw_full_name is not null then
       raw_first_name := split_part(raw_full_name, ' ', 1);
       raw_last_name := split_part(raw_full_name, ' ', 2);
    else
       raw_first_name := split_part(new.email, '@', 1);
    end if;
  end if;

  insert into public.profiles (id, email, username, is_admin)
  values (
    new.id, 
    new.email, 
    coalesce(raw_full_name, raw_first_name || ' ' || coalesce(raw_last_name, ''), split_part(new.email, '@', 1)),
    false
  );
  return new;
end;
$$ language plpgsql security definer;

-- Trigger to call the function on new user creation
create or replace trigger on_auth_user_created
  after insert on auth.users
  for each row execute procedure public.handle_new_user();

-- ==========================================
-- 7. ACCOUNT MANAGEMENT (RPC Functions)
-- ==========================================

-- Function to allow users to delete their own account
-- This must be SECURITY DEFINER to allow users to delete their own auth record
create or replace function delete_user_account()
returns void
language plpgsql
security definer
as $$
begin
  -- Delete the user from auth.users table
  -- This will cascade to public.profiles if foreign key is set correctly
  delete from auth.users where id = auth.uid();
end;
$$;
